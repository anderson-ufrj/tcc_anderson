// Language management
let currentLanguage = localStorage.getItem('language') || 'pt';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    updatePageTranslations();
}

function translate(key) {
    return translations[currentLanguage]?.[key] || translations['pt'][key] || key;
}

function updatePageTranslations() {
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = translate(key);
    });
    
    // Update all elements with data-translate-placeholder attribute
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        element.placeholder = translate(key);
    });
    
    // Update page title
    const pageTitle = document.querySelector('title');
    if (pageTitle) {
        const currentPage = window.location.pathname.split('/').pop() || 'index.html';
        if (currentPage === 'index.html') {
            pageTitle.textContent = `${translate('hero.title')} | IFSULDEMINAS`;
        } else if (currentPage === 'conteudo.html') {
            pageTitle.textContent = `${translate('card.content.title')} - ${translate('hero.title')} | IFSULDEMINAS`;
        } else if (currentPage === 'chat-ia.html') {
            pageTitle.textContent = `${translate('card.chat.title')} - ${translate('hero.title')} | IFSULDEMINAS`;
        }
    }
}

// Initialize translations
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLanguage);
});